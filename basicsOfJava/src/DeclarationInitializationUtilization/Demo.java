package DeclarationInitializationUtilization;

public class Demo {
    public static void main(String[] args)
    {
        //declaration :its the process of declaring memory block to store the value
        int a;
        int b;

        //initialization:its the process of storing the value inside declared memory blocks
        a=20;
        b=30;

        //utilization :its the process of utilizing the initialized value through variable
        System.out.print(a);
        System.out.print(b);

    }
}
