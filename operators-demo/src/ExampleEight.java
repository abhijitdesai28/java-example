public class ExampleEight
{
    public static void main(String[] args)
    {
        //create variables
        int a=10;
        int var;

        //assign value using = operator
        var =a;// var=10;
        System.out.println(var);//10

        //assign value using =+
        var +=a;//var =var+a;
                //      =10+10
        System.out.println(var);//20

        //assign value using *=
        var *=a; //var=var*a;
                 //    =200
        System.out.println(var);//200

        //assign value using /=
        var/=a; //var=var/a;
                //   =200/10
        System.out.println(var);//20

        //assign value using %=
        var%=a; //var=var%a;
                //    =20%10
        System.out.println(var);//0

    }
}
